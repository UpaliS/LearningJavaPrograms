package ex_10_For_Loop;

public class Lab101_For_If_Loop {

    public static void main(String[] args) {
        for (int shruti=0; shruti<18; shruti++) {
            if (shruti > 15) {
                System.out.println("iPhone Gift");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
