package ex_04_Operators;

public class Lab036_Arithmatic_Operators {

    public static void main(String[] args) {

        // Arithmatic operations
        // + (addition)
        // - (substraction)
        // * (multiplication)
        // / (division)
        // % (Modulud) | Modulus - remainder

        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // it will only give the integer value
        System.out.println(a/c); // it will give the decimal value

    }
}
