package ex_04_Operators;

public class Lab038_Operator_Modulus {

    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b);

        // mod --> remainder
        // 10 | 20 | 2 - Quotient
        //    | 20 |
        //    -------
        //      0 - remainder or modulus value

        System.out.println(13%7);
    }
}
