package ex_07_Increment_Decrement_Operator;

public class Lab071_Increment_Operator3 {

    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);

        //Line No. | a | Exp(result)
        // 6 | 10 | NA
        // 7 | 11 | 11
    }
}
