package ex_10_For_Loop;

public class Lab107_For_Loop9 {

    public static void main(String[] args) {
        for (int i =0; i<=10; i++) {
            System.out.println(i); // It will print 0 to 10, 11 times
        }
        for (int i = 1; i<=10; i++) {
            System.out.println(i); // It will print 1 to 10, 10times
        }
    }
}
