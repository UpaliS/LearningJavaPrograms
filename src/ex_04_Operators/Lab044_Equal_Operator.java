package ex_04_Operators;

public class Lab044_Equal_Operator {

    public static void main(String[] args) {
        // == --> compare the values

        System.out.println(10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 >= 10);
    }
}
