package ex_09_SwitchCase;

public class Lab087_InterviewQnA2 {

    public static void main(String[] args) {
        char ch = 'A';
        switch (ch) {
            case 65:
                System.out.println("Match ASCII");
                break;
            default:
                System.out.println("No Match");
                break;
        }
    }
}
